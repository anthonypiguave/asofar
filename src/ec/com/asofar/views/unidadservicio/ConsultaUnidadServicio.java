    public VeUnidadServicio devuelveObjeto(Long id, List<VeUnidadServicio> listabod) {
        VeUnidadServicio doc = null;
        for (int i = 0; i < listabod.size(); i++) {
            if (Objects.equals(listabod.get(i).getIdUnidadServicio(), id)) {
                doc = listabod.get(i);
                break;
            }
        }
        return doc;
    }
