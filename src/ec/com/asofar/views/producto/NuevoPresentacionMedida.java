    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        int r = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro de guardar los datos?", "", JOptionPane.YES_NO_OPTION);
        PrMedidas obj = new PrMedidas();

        if (r == JOptionPane.YES_OPTION) {
            try {

                obj.setPrArticulo(articuloObjeto);
                obj.setPrTipoPresentacion(tipopresentacion);
                obj.setPrTipoMedidas(tipomedida);
                obj.setUsuarioCreacion(seUsuario.getUsuario());
                obj.setFechaCreacion(d);
                obj.setEstado("A");

                cont.create(obj);

                JOptionPane.showMessageDialog(null, "GUARDADO EXITOSAMENTE");
                setVisible(false);

            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

